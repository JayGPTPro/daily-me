def render_article(art):
    body = art["body"]
    image_block = ""
    if art.get("image"):
        image_block = (
            f'<div class="article-image-wrap">'
            f'<img src="{art["image"]}" alt="{art["title"]}" class="article-image" loading="lazy" '
            f'onerror="this.parentElement.style.display=\'none\'">'
            f'</div>'
        )

    html = ARTICLE_TPL
    html = html.replace("{{ARTICLE_TITLE}}", art["title"])
    html = html.replace("{{ARTICLE_CATEGORY}}", art["category"])
    html = html.replace("{{ARTICLE_DATE}}", DATE_HEBREW)
    html = html.replace("{{ARTICLE_ID}}", art["id"])
    html = html.replace("{{ARTICLE_BODY}}", body)
    html = html.replace("{{SOURCE_URL}}", art["source"])

    # Conditional image block - replace the whole {{#ARTICLE_IMAGE}}...{{/ARTICLE_IMAGE}}
    html = re.sub(
        r"\{\{#ARTICLE_IMAGE\}\}.*?\{\{/ARTICLE_IMAGE\}\}",
        image_block,
        html,
        flags=re.DOTALL,
    )
    # Replace remaining placeholders
    html = html.replace("{{ARTICLE_IMAGE}}", art.get("image", ""))
    return html


