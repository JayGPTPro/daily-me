def main():
    # Ensure directories
    ARTICLES_DIR.mkdir(parents=True, exist_ok=True)
    ARCHIVE_DIR.mkdir(parents=True, exist_ok=True)

    # Write all article pages
    all_articles = ai_news + politics + war + world + amazon + gadgets + sports + entertainment
    for article in all_articles:
        article_path = ARTICLES_DIR / f"{DATE_SLUG}-{article['id']}.html"
        article_path.write_text(build_article_html(article), encoding="utf-8")

    # Write main index.html
    main_html = build_main_html(is_archive=False)
    (DOCS / "index.html").write_text(main_html, encoding="utf-8")

    # Write archive version
    archive_html = build_main_html(is_archive=True)
    archive_path = ARCHIVE_DIR / f"{DATE_SLUG}-evening.html"
    archive_path.write_text(archive_html, encoding="utf-8")

    # Update editions.json
    editions_file = ARCHIVE_DIR / "editions.json"
    if editions_file.exists():
        editions = json.loads(editions_file.read_text(encoding="utf-8"))
    else:
        editions = []

    new_entry = {"date": DATE_SLUG, "edition": "evening", "file": f"{DATE_SLUG}-evening.html"}
    # Remove existing entry for this date+edition if any
    editions = [e for e in editions if not (e.get("date") == DATE_SLUG and e.get("edition") == "evening")]
    # Insert at the beginning (newest first)
    editions.insert(0, new_entry)
    editions_file.write_text(json.dumps(editions, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"✅ Generated {len(all_articles)} article pages")
    print(f"✅ Wrote {DOCS / 'index.html'}")
    print(f"✅ Wrote {archive_path}")
    print(f"✅ Updated editions.json")


if __name__ == "__main__":
    main()
